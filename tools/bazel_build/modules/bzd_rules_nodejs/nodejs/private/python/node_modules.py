import argparse
import os
import pathlib
import tarfile


def extractStore(store: pathlib.Path, destination: pathlib.Path) -> None:
	"""Extract a package store archive."""

	with tarfile.open(store, "r") as tar:
		tar.extractall(destination, filter="data")


if __name__ == "__main__":
	parser = argparse.ArgumentParser(description="Setup the node_modules directory.")
	parser.add_argument(
		"--output",
		type=pathlib.Path,
		required=True,
		help="The destination path of the node_modules directory.",
	)
	parser.add_argument(
		"--store",
		type=pathlib.Path,
		action="append",
		default=[],
		help="Store archive to be added, can be used multiple times.",
	)
	parser.add_argument(
		"--top-level",
		metavar=("MODULE_NAME", "CANONICAL_NAME"),
		nargs=2,
		action="append",
		default=[],
		help="Top level modules to be added, can be used multiple times.",
	)
	args = parser.parse_args()

	for store in args.store:
		extractStore(store=store, destination=args.output / ".store")

	for moduleName, canonicalName in args.top_level:
		topLevel = args.output / moduleName
		topLevel.parent.mkdir(parents=True, exist_ok=True)
		up = [".."] * (len(canonicalName.split("/")) - 1)
		os.symlink("/".join(up + [".store", canonicalName]), topLevel)
